'use strict';

const logger = require('../../logger/winston');
const refreshTokenHelper = require('./sql-helper-refresh-token');

// ////////////////////////////////////////////////////////////////////////////////// //
// PUBLIC
// ////////////////////////////////////////////////////////////////////////////////// //

const logout = async (req, res, next) => {
    logger.debug('accounts/sql-helper-logout.logout', req.log);

    try {
        await refreshTokenHelper.revoke(req.tokens.refreshToken.uuid);
        res.status(204).send();
    } catch (err) {
        return next(err);
    }
};

// ////////////////////////////////////////////////////////////////////////////////// //
// EXPORTS
// ////////////////////////////////////////////////////////////////////////////////// //

module.exports = {
    logout,
};
